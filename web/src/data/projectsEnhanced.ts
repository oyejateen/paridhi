export function getProjectsByCategory(category: ProjectCategory): EnhancedProject[] {
  return enhancedProjects.filter((p) => p.category === category)
}

export function getProjectById(id: string): EnhancedProject | undefined {
  return enhancedProjects.find((p) => p.id === id)
}

export function getProjectsByStatus(status: 'ongoing' | 'completed' | 'handovered'): EnhancedProject[] {
  return enhancedProjects.filter((p) => p.status === status)
}

export function getNearbyProjects(lat: number, lng: number, radiusKm: number = 5): EnhancedProject[] {
  return enhancedProjects.filter((project) => {
    const distance = calculateDistance(lat, lng, project.lat, project.lng)
    return distance <= radiusKm
  })
}

// Haversine formula to calculate distance between two coordinates
export function calculateDistance(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371 // Earth's radius in kilometers
  const dLat = ((lat2 - lat1) * Math.PI) / 180
  const dLng = ((lng2 - lng1) * Math.PI) / 180
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) * Math.sin(dLng / 2) * Math.sin(dLng / 2)
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
  return R * c // Distance in kilometers
}
